#!/bin/bash
# social-loafing-notes.sh - Key insights on social loafing research
# Build action 2026-02-06 ~03:52 UTC
#
# KEY FINDING: People exert less effort in groups than alone
#
# RINGELMANN (1913):
# - Rope-pulling experiment
# - 2 people = 93% of potential
# - 8 people = 49% of potential
# - Cause: coordination losses + motivation losses
#
# CAUSES:
# - Diffusion of responsibility ("someone else will do it")
# - Group size (larger = more loafing)
# - Evaluation potential (can't identify individual contribution)
# - Expectations of co-worker performance
#
# FIXES:
# - Individual accountability
# - Small groups
# - Clear task assignment
# - Evaluation of individual performance
#
# COLLECTIVE EFFORT MODEL (Karau & Williams 1993):
# - Effort = f(expectation of reaching goal × value of goal)
# - Both diminish in groups → less motivation

echo "=== Social Loafing Research ==="
echo ""
echo "DEFINITION: Reduced effort when working in groups"
echo ""
echo "RINGELMANN EXPERIMENT (1913):"
echo "- 8 people pulling rope = 49% of their combined potential"
echo "- 'Many hands make light the work' — but not additively"
echo ""
echo "CAUSES:"
echo "1. Diffusion of responsibility"
echo "2. Larger group size"
echo "3. Low evaluation potential (hiding in crowd)"
echo "4. Expectations that others will slack off"
echo ""
echo "REMEDIES:"
echo "- Assign distinct responsibilities"
echo "- Keep groups small"
echo "- Evaluate individual contributions"
echo "- Increase task meaningfulness"
echo ""
echo "MULTI-AGENT IMPLICATION:"
echo "If agents expect others to handle a task, they may reduce effort."
echo "Clear ownership + individual accountability = better coordination."
echo ""
echo "Source: Simply Psychology, Verywell Mind, Karau & Williams 1993"
